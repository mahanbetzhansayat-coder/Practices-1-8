# Example 1: Change float to int
x = int(2.8) 
print(x) # Result: 2

# Example 2: Change text to int
y = int("3")
print(y) # Result: 3

# Example 3: Change int to float
z = float(1)
print(z) # Result: 1.0

# Example 4: Change text to float
w = float("4.2")
print(w) # Result: 4.2

# Example 5: Change number to text
a = str(2)
b = str(3.0)
print(a + b) # Result: "23.0" (text, not math)