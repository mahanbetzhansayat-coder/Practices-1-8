# Example 1: Integers (Whole numbers)
x = 1
y = 35656222554887711
z = -3255522
print(type(x), type(y), type(z))

# Example 2: Float (Decimal numbers)
x = 1.10
y = 1.0
z = -35.59
print(type(x), type(y), type(z))

# Example 3: Scientific numbers "e"
x = 35e3
y = 12E4
print(x)
print(y)

# Example 4: Complex numbers
x = 3+5j
y = 5j
z = -5j
print(type(x))

# Example 5: Random number
import random
print(random.randrange(1, 10))