import re
import os

# 1. Открываем и читаем наш чек
with open(r'raw.txt', 'r', encoding='utf-8') as file:
    text = file.read()

print("=== ЗАДАНИЕ: ПАРСИНГ ЧЕКА ===\n")

# --- Метод 1: re.search() (Найти первое совпадение) ---
# Ищем дату. Паттерн: две цифры, точка, две цифры, точка, четыре цифры
date_match = re.search(r"\d{2}\.\d{2}\.\d{4}", text)
print("Дата чека:", date_match.group())

# Ищем время. Паттерн: две цифры, двоеточие, две цифры, двоеточие, две цифры
time_match = re.search(r"\d{2}:\d{2}:\d{2}", text)
print("Время пробития:", time_match.group())

# Ищем ИТОГО. Паттерн: слово "ИТОГО:", перенос строки (\n) и захватываем всё, что дальше (.*)
total_match = re.search(r"ИТОГО:\n(.*)", text)
print("Итоговая сумма:", total_match.group(1))


# --- Метод 2: re.findall() (Найти ВСЕ совпадения) ---
# Ищем все цены. Паттерн: цифры (\d+), потом запятая, потом ровно две цифры (\d{2})
all_prices = re.findall(r"\d+,\d{2}", text)
print("\nСколько всего цен найдено в чеке:", len(all_prices))
print("Вот первые 5 цен для примера:", all_prices[:5])


# --- Метод 3: re.sub() (Заменить текст) ---
# Чтобы сумму "18 009,00" можно было считать в Python, нужно убрать пробел.
# Паттерн: \s (любой пробел). Заменяем на "" (пустоту).
raw_total = total_match.group(1)
clean_total = re.sub(r"\s", "", raw_total)
print("\nСумма без пробелов (re.sub):", clean_total)


# --- Метод 4: re.split() (Разделить текст) ---
# Разрежем весь чек на куски по слову "Стоимость", чтобы разделить товары
blocks = re.split(r"Стоимость", text)
print("\nЧек разделен на части с помощью re.split. Количество частей:", len(blocks))